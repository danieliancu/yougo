export function translate(locale: string, key: string, params: Record<string, string | number> = {}) {
  const value = dictionaries[locale]?.[key] ?? dictionaries.en[key] ?? key;

  return Object.entries(params).reduce((text, [param, replacement]) => text.replaceAll(`:${param}`, String(replacement)), value);
}

export function useT() {
  const { locale = 'ro' } = usePage<{ locale?: string }>().props;

  return (key: string, params?: Record<string, string | number>) => translate(locale, key, params);
}
